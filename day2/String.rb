p message_of_the_day[1] #u
p message_of_the_day[4] #[space]
p message_of_the_day[5..8] #is b this gives you a range
p message_of_the_day[1..3] #uby
p message_of_the_day[1,4] #this is a slicer, means that it starts at character 1
                          # then goes to the 4th character
p message_of_the_day[10,2] #tt
